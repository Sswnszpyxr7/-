TALK_GAIN = {"affection": 4, "trust": 3, "safety": 3, "fate_resonance": 2}
GIFT_GAIN_LIKED = {"affection": 8, "trust": 4, "safety": 4, "fate_resonance": 4}
GIFT_GAIN_NORMAL = {"affection": 4, "trust": 2, "safety": 2, "fate_resonance": 2}


def _partners(state):
    return [c for c in state.contracts.values() if c.get("unlocked")]


def heart_talk(state, talked):
    """个别谈心。每名契约者每日只能获得一次关系成长。"""
    cs = _partners(state)
    opts = [c["name"] + (" (本次已谈心)" if c["id"] in talked else "")
            for c in cs]
    idx = ui.choose("和谁谈心?", opts, allow_cancel=True)
    if idx < 0:
        return
    c = cs[idx]
    if c["id"] in talked:
        ui.slow_print(f"{c['name']}笑着摆摆手:「今天说得够多啦。下次再来。」")
        return
    daily_key = f"heart_talk:{c['id']}"
    if not state.consume_daily_use(daily_key, limit=1):
        ui.slow_print(f"{c['name']}陪你又坐了一会儿。今天的心意已经充分传达，明天再来吧。")
        talked.add(c["id"])
        return
    talked.add(c["id"])
    ui.line()
    ui.story(random.choice(TALKS[c["id"]]))
    contract.add_bond(state, c["id"], TALK_GAIN)
    gain = 2 if state.has_building("cottage") else 1
    state.belonging += gain
    ui.slow_print(f"※ 归属感 +{gain} [当前: {state.belonging}]")
    ui.pause()


def give_gift(state):
    """送礼:消耗一件道具,提升契约四维(偏好道具加成更高)。"""
    cs = _partners(state)
    items = {k: v for k, v in state.inventory.items() if v > 0}
    if not items:
        ui.slow_print("行囊空空。先去外面探索,或等桃源的田地结果吧。")
        return
    idx = ui.choose("送给谁?", [c["name"] for c in cs], allow_cancel=True)
    if idx < 0:
        return
    c = cs[idx]
    names = list(items.keys())
    liked = GIFT_LIKES.get(c["id"])
    gi = ui.choose(f"送什么给{c['name']}?",
                   [f"{n} ×{cnt}" + (" ♥偏好" if n == liked else "")
                    for n, cnt in items.items()],
                   allow_cancel=True)
    if gi < 0:
        return
