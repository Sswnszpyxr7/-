def herb_quest_turn_in(state):
    """医院:交付药草。"""
    from systems import quest
    if state.inventory.get("药草", 0) < 3:
        n = state.inventory.get("药草", 0)
        ui.slow_print(f"(药草还不够。当前 {n}/3 株——去森林里「深入探索」找找吧。)")
        return
    state.inventory["药草"] -= 3
    ui.story("""
「哎呀,品相真好!」护士长眉开眼笑地接过药草,
「不愧是忍者,谢谢你啦。这些拿去,是谢礼。」
""")
    quest.complete_quest(state, "herb_gathering")
    state.flags["herb_quest_active"] = False
